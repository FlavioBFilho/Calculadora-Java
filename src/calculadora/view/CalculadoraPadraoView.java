    private void jBtnMaisMenosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnMaisMenosActionPerformed
        double n = Double.parseDouble(jTxtTela.getText());
        n = n*-1;
        jTxtTela.setText(trataInteiro(n));
    }//GEN-LAST:event_jBtnMaisMenosActionPerformed

    private void jBtnBackActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnBackActionPerformed
        String apagado = jTxtTela.getText().substring(0, jTxtTela.getText().length()-1);
        jTxtTela.setText(apagado);
        
        if(jTxtTela.getText().isEmpty()){
            jTxtTela.setText("0");
        }
    }//GEN-LAST:event_jBtnBackActionPerformed

    private void operacao(java.awt.event.ActionEvent evt){
        double x = Double.parseDouble(jTxtTela.getText());
        char op = evt.getActionCommand().charAt(0);
        calc.setA(x);
        calc.setOp(op);
        jTxtTela.setText("");
    }
    
    private void digitar(java.awt.event.ActionEvent evt){
        if(jTxtTela.getText().compareTo("0")==0){
            jTxtTela.setText("");
        }
        
        if(jTxtTela.getText().length()<=10){
            if(evt.getActionCommand().charAt(0) != '.' || jTxtTela.getText().indexOf('.') == -1){
                jTxtTela.setText(jTxtTela.getText()+evt.getActionCommand());
            }
        }
    }
    
    
    
