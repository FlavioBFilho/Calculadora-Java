    private String trataInteiro(Double x1){
        
        int x2 = x1.intValue();
        
        if(x1>x2){
            return String.format("%.5f", x1).replace(',', '.');
        }
        else{
            return Integer.toString(x2);
        }
        
    }
    
    private void jBtnIgualActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnIgualActionPerformed
        double y = Double.parseDouble(jTxtTela.getText());
        calc.setB(y);
        calc.igual();
        jTxtTela.setText(trataInteiro(calc.getRes()));
        
    }//GEN-LAST:event_jBtnIgualActionPerformed

